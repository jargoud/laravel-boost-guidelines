## Jargoud's Laravel & PHP Coding Guidelines

- This codebase follows Jargoud's Laravel & PHP guidelines.
- Always activate these skills whenever writing, editing, reviewing, or formatting Laravel or PHP code:
  - `jargoud-php-coding-quality` — cross-cutting principles, quality bar, scope discipline, and validation.
  - `jargoud-php` — PHP 8+ strict typing and modern idioms (`strict_types`, promotion, `readonly`, enums).
  - `jargoud-laravel` — Laravel architecture under `app/` (Actions, DTOs, Integrations, Observers, Pest).
